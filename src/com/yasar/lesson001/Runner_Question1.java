package com.yasar.lesson001;

public class Runner_Question1 {
	public static void main(String[] args) {
		/**
		 * Sayısal değişkenler primitive tür değişkenler --> Stack bellek üzerinde
		 * tutulurlar.
		 * 
		 */

		byte myByte = 2;
		System.out.println(myByte);
		short myShort = 150;
		System.out.println(myShort);
		int myInt = 66000;
		System.out.println(myInt);
		long myLong = 8971294712897412L;
		System.out.println(myLong);
		float myFloat = 12.41f;
		System.out.println(myFloat);
		double myDouble = 44.112;
		System.out.println(myDouble);
		char myChar = 'a';
		System.out.println(myChar);

		// Mantıksal değişkenler

		boolean myBoolean1 = true;
		boolean myBoolean2 = false;
		System.out.println(myBoolean1);
		System.out.println(myBoolean2);

		// ifadesel değişken

		String myString = "Yasar";
		System.out.println(myString);
	}

}
